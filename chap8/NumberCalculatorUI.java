    private void cbnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbnCalculateActionPerformed
        float num1, num2, result = 0;
        Float.parseFloat(txtFirstNum.getText());
        num1 = Float.parseFloat(txtSecondNum.getText());
        num2 = Float.parseFloat(txtSecondNum.getText());
        //result = num1+num2;
        //txtResult.setText(String.valueOf(result));
        String sign = (String) cbxSign.getSelectedItem();
        if (sign.equals("+")) {
            result = num1 + num2;
        }
        if (sign.equals("-")) {
            result = num1 - num2;
        }
        if (sign.equals("*")) {
            result = num1 * num2;
        }
        if (sign.equals("/")) {
            result = num1 / num2;
        }
        txtResult.setText(String.valueOf(result));


    }//GEN-LAST:event_cbnCalculateActionPerformed

